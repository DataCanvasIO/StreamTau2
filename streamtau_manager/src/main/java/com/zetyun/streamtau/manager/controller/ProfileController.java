package com.zetyun.streamtau.manager.controller;

import com.zetyun.streamtau.manager.service.ProfileService;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.http.MediaType;
import org.springframework.stereotype.Controller;
import org.springframework.web.bind.annotation.GetMapping;
import org.springframework.web.bind.annotation.PathVariable;
import org.springframework.web.bind.annotation.RequestMapping;
import org.springframework.web.bind.annotation.ResponseBody;

@Controller
@RequestMapping("/profile")
public class ProfileController {
    @Autowired
    private ProfileService profileService;

    @GetMapping(value = "/{element}", produces = {MediaType.APPLICATION_JSON_VALUE})
    @ResponseBody
    public String getProfile(@PathVariable("element") String element) {
        return profileService.get(element);
    }
}
